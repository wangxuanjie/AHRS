/* USER CODE BEGIN 0 */
#define GCS_RX_LEN 64
uint8_t _gcs_receive_cnt = 0;
uint8_t _gcs_receive_buff[GCS_RX_LEN] = {};
/* USER CODE END 0 */

UART_HandleTypeDef huart2;
DMA_HandleTypeDef hdma_usart2_tx;
DMA_HandleTypeDef hdma_usart2_rx;
uint8_t uart_tx_dma[512] = {0};
bool _tx_complete = true;

/* USART2 init function */

